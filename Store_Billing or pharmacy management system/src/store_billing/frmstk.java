    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
       if(txtprc.getText()==""||txtqty.getText()==""
               ||txtmfgdat.getText()==""||txtexpdat.getText()=="")
           JOptionPane.showMessageDialog(this,"Please Fill Entries");
       else
       {
           try {
               clsstk obj=new clsstk();
               clsstkprp objprp=new clsstkprp();
               objprp.setstkprc(Float.parseFloat(txtprc.getText()));
               objprp.setstkqty(Integer.parseInt(txtqty.getText()));
               objprp.setstkdat(new Date());
               objprp.setstkmedcod(k.get
                        (cmbmed.getSelectedIndex()).getmedcod());
               Date mfgdat=new SimpleDateFormat("dd/MM/yyyy").parse(txtmfgdat.getText());
               Date expdat=new SimpleDateFormat("dd/MM/yyyy").parse(txtexpdat.getText());
               objprp.setstkmnfdat(mfgdat);
               objprp.setstkexpdat(expdat);
               obj.save_rec(objprp);
           } catch (ParseException ex) {
               Logger.getLogger(frmstk.class.getName()).log(Level.SEVERE, null, ex);
           }
     loaddata();
       }
    }//GEN-LAST:event_jButton1ActionPerformed

    private void cmbmedActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_cmbmedActionPerformed
       int a=k.get(cmbmed.getSelectedIndex()).getmedcod();
        
        clsstk obj=new clsstk();
    List<clsstkprp> k=obj.disp_rec(a);
    DefaultTableModel mod=(DefaultTableModel)jTable2.getModel();
    mod.setRowCount(0);
    for(clsstkprp p:k)
    {
        Object[] rowdata=new Object[5];
        rowdata[0]=p.getstkcod();
        rowdata[1]=p.getstkqty();
        rowdata[2]=p.getstkprc();
        rowdata[3]=p.getstkmnfdat();
        rowdata[4]=p.getstkexpdat();
        mod.addRow(rowdata);
    }
    jTable2.setModel(mod);
        
    }//GEN-LAST:event_cmbmedActionPerformed
