    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        // TODO add your handling code here:
        if(jTable1.getSelectedRow()==-1)
  JOptionPane.showMessageDialog(this,"Please select stock from which you want to purchase");
        else
        {
           if(Integer.parseInt(txtqty.getText())>
                   (int)jTable1.getValueAt(jTable1.
                           getSelectedRow(),3))
           {
  JOptionPane.showMessageDialog(this,"Quantity low in stock selected");
           }
           else
           {
               clstmpbil obj=new clstmpbil();
               obj.setQtypur(Integer.parseInt(txtqty.getText()));
               obj.setMednam(cmbmed.getSelectedItem().toString());
               obj.setMedcod(k.get(cmbmed.getSelectedIndex())
                       .getmedcod());
               obj.setStkcod((int)jTable1.getValueAt
        (jTable1.getSelectedRow(), 0));
               obj.setPrc((float)jTable1.getValueAt
        (jTable1.getSelectedRow(),4));
               clstmpbil.arr.add(obj);
            loadbill();   
               
           }
        }
    }//GEN-LAST:event_jButton1ActionPerformed
