    private void jTable2MouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_jTable2MouseClicked
        int idx=jTable2.getSelectedRow();
      scod=(int)jTable2.getValueAt(idx, 0);
       clsstk obj1=new clsstk();
      clsstkprp p1=obj1.find_rec(scod);
      clsmed obj=new clsmed();
      clsmedprp p=obj.find_rec(p1.getstkmedcod());
      txtprc.setText(jTable2.getValueAt(idx, 2).toString());
      txtqty.setText(jTable2.getValueAt(idx, 1).toString());
      txtmfgdat.setText(jTable2.getValueAt(idx, 3).toString());
      txtexpdat.setText(jTable2.getValueAt(idx, 4).toString());
     
      cmbmed.setSelectedItem(p.getmednam());
      
        jButton2.setEnabled(true);
        jButton4.setEnabled(true);
    }//GEN-LAST:event_jTable2MouseClicked

