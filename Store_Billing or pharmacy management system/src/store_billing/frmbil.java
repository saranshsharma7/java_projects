    private void cmbmedItemStateChanged(java.awt.event.ItemEvent evt) {//GEN-FIRST:event_cmbmedItemStateChanged
        // TODO add your handling code here:
        if(evt.getStateChange()==ItemEvent.SELECTED)
        {
   int mcod=k.get(cmbmed.getSelectedIndex()).getmedcod();
            clsstk obj=new clsstk();
   List<clsstkprp> k=obj.dspmedstk(mcod);
   DefaultTableModel mod=(DefaultTableModel)jTable1.getModel();
   mod.setRowCount(0);
   for(clsstkprp p:k)
   {
       if(p.getstkqty()>0)
       {
           Object[] rowdata=new Object[5];
           rowdata[0]=p.getstkcod();
           rowdata[1]=p.getstkmnfdat();
           rowdata[2]=p.getstkexpdat();
           rowdata[3]=p.getstkqty();
           rowdata[4]=p.getstkprc();
           mod.addRow(rowdata);
       }
   }
   jTable1.setModel(mod);
        }
    }//GEN-LAST:event_cmbmedItemStateChanged

